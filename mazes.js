// Seeded RNG function
function SeededRNG (seed) {
  return function () {
    seed = (seed * 16807) % 2147483647
    return (seed - 1) / 2147483646
  }
}

// Function to shuffle an array using a custom random number generator
function shuffleArray (array, rng) {
  for (let i = array.length - 1; i > 0; i--) {
    const j = Math.floor(rng() * (i + 1))
    ;[array[i], array[j]] = [array[j], array[i]]
  }
}

// Maze generation function
function generateMaze (width, height, seed) {
  let random = SeededRNG(seed)
  let maze = new Array(height)

  // Initialize all cells as walls
  for (let y = 0; y < height; y++) {
    maze[y] = new Array(width).fill(1)
  }

  // Carve paths inside the maze, leaving the outer border as walls
  function carve (x, y) {
    const directions = [
      [1, 0],
      [0, 1],
      [-1, 0],
      [0, -1]
    ]
    shuffleArray(directions, random)

    for (let i = 0; i < directions.length; i++) {
      const dx = directions[i][0],
        dy = directions[i][1]
      const nx = x + dx * 2,
        ny = y + dy * 2

      // Check if the new cell is within the inner area (excluding borders)
      if (
        nx > 0 &&
        nx < width - 1 &&
        ny > 0 &&
        ny < height - 1 &&
        maze[ny][nx] === 1
      ) {
        maze[ny][nx] = 2 // Set path to 2 (pellets)
        maze[y + dy][x + dx] = 2 // Set path to 2 (pellets)
        carve(nx, ny)
      }
    }
  }

  // Start carving from a random point within the inner area
  const startX = Math.floor(random() * (width - 2)) + 1
  const startY = Math.floor(random() * (height - 2)) + 1
  maze[startY][startX] = 2

  carve(startX, startY)

  return maze
}

